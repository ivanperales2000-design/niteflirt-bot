#!/usr/bin/env node

const { chromium } = require('playwright');
require('dotenv').config();
const S = require('./src/selectors');
const axios = require('axios');

const SLEEP = ms => new Promise(r => setTimeout(r, ms));
const jitter = (a,b) => Math.floor(Math.random()*(b-a+1))+a;

class SmartBot {
  constructor() {
    this.browser = null;
    this.page = null;
    this.isLoggedIn = false;
    this.currentChat = null;
  }

  async init() {
    console.log('ü§ñ Iniciando Smart Bot de Niteflirt...\n');
    
    try {
      // Cerrar cualquier proceso anterior
      await this.killPreviousProcesses();
      
      // Iniciar navegador
      this.browser = await chromium.launch({ 
        headless: false, 
        slowMo: 50,
        args: ['--no-sandbox', '--disable-setuid-sandbox', '--disable-dev-shm-usage']
      });
      
      const context = await this.browser.newContext({
        viewport: { width: 1280, height: 720 },
        userAgent: 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36'
      });
      
      this.page = await context.newPage();
      
      // Configurar timeouts
      this.page.setDefaultTimeout(30000);
      this.page.setDefaultNavigationTimeout(30000);
      
      console.log('‚úÖ Navegador iniciado correctamente');
      return true;
      
    } catch (error) {
      console.error('‚ùå Error iniciando navegador:', error.message);
      return false;
    }
  }

  async killPreviousProcesses() {
    try {
      console.log('üîß Cerrando procesos anteriores...');
      await new Promise((resolve) => {
        const { exec } = require('child_process');
        exec('taskkill /f /im chrome.exe /t', (error) => {
          if (error) console.log('‚ö†Ô∏è No se encontraron procesos de Chrome para cerrar');
          resolve();
        });
      });
      await SLEEP(1000);
    } catch (error) {
      console.log('‚ö†Ô∏è Error cerrando procesos:', error.message);
    }
  }

  async login() {
    console.log('üîê Iniciando proceso de login...');
    
    try {
      // Ir a la p√°gina de login
      await this.page.goto(S.urls.login, { waitUntil: 'domcontentloaded' });
      await SLEEP(2000);
      
      // Verificar si ya est√° logueado
      const alreadyLoggedIn = await this.page.locator(S.auth.loggedIn).count() > 0;
      if (alreadyLoggedIn) {
        console.log('‚úÖ Ya est√° logueado');
        this.isLoggedIn = true;
        return true;
      }
      
      // Intentar login autom√°tico
      console.log('üìß Intentando login autom√°tico...');
      
      // Buscar campo de email
      const emailField = this.page.locator(S.auth.emailField);
      if (await emailField.count() > 0) {
        await emailField.first().fill(process.env.NF_EMAIL);
        console.log('‚úÖ Email ingresado');
      } else {
        console.log('‚ö†Ô∏è No se encontr√≥ campo de email');
      }
      
      // Buscar campo de contrase√±a
      const passwordField = this.page.locator(S.auth.passwordField);
      if (await passwordField.count() > 0) {
        await passwordField.first().fill(process.env.NF_PASS);
        console.log('‚úÖ Contrase√±a ingresada');
      } else {
        console.log('‚ö†Ô∏è No se encontr√≥ campo de contrase√±a');
      }
      
      // Buscar bot√≥n de login
      const loginButton = this.page.locator(S.auth.loginButton);
      if (await loginButton.count() > 0) {
        await loginButton.first().click();
        console.log('‚úÖ Bot√≥n de login presionado');
        await SLEEP(3000);
      } else {
        console.log('‚ö†Ô∏è No se encontr√≥ bot√≥n de login');
      }
      
      // Esperar login manual si es necesario
      console.log('‚è≥ Esperando completar login...');
      await this.page.waitForSelector(S.auth.loggedIn, { timeout: 300000 });
      
      this.isLoggedIn = true;
      console.log('‚úÖ Login completado exitosamente\n');
      return true;
      
    } catch (error) {
      console.error('‚ùå Error en login:', error.message);
      return false;
    }
  }

  async navigateToChat() {
    console.log('üí¨ Navegando a la secci√≥n de chat...');
    
    try {
      // Ir directamente a la URL de chat
      await this.page.goto(S.urls.chat, { waitUntil: 'networkidle' });
      await SLEEP(3000);
      
      // Verificar que estamos en la p√°gina correcta
      const chatElements = await this.page.locator(S.chat.threadList).count();
      if (chatElements > 0) {
        console.log('‚úÖ Navegaci√≥n al chat completada');
        return true;
      } else {
        console.log('‚ö†Ô∏è No se detectaron elementos de chat, intentando navegaci√≥n manual...');
        
        // Intentar hacer clic en enlaces de navegaci√≥n
        const inboxLink = this.page.locator(S.navigation.inboxLink);
        if (await inboxLink.count() > 0) {
          await inboxLink.first().click();
          await SLEEP(3000);
          console.log('‚úÖ Navegaci√≥n manual completada');
          return true;
        }
      }
      
      return false;
      
    } catch (error) {
      console.error('‚ùå Error navegando al chat:', error.message);
      return false;
    }
  }

  async findAndOpenChat(targetUsername = null) {
    console.log(`üîç Buscando chat${targetUsername ? ` de ${targetUsername}` : ' disponible'}...`);
    
    try {
      // Esperar a que carguen los chats
      await SLEEP(2000);
      
      // Buscar elementos de chat
      const threads = this.page.locator(S.chat.threadItem);
      const count = await threads.count();
      
      console.log(`üì¨ Encontrados ${count} chats disponibles`);
      
      if (count === 0) {
        console.log('‚ùå No se encontraron chats disponibles');
        return false;
      }
      
      let selectedThread = null;
      
      // Si se especifica un usuario, buscarlo
      if (targetUsername) {
        for (let i = 0; i < count; i++) {
          const thread = threads.nth(i);
          const threadText = await thread.innerText().catch(() => '');
          
          if (threadText.toLowerCase().includes(targetUsername.toLowerCase())) {
            console.log(`‚úÖ Encontrado chat de ${targetUsername} en posici√≥n ${i + 1}`);
            selectedThread = thread;
            break;
          }
        }
      }
      
      // Si no se encontr√≥ el usuario espec√≠fico o no se especific√≥, usar el primero
      if (!selectedThread) {
        selectedThread = threads.nth(0);
        const firstThreadText = await selectedThread.innerText().catch(() => '');
        console.log(`üì¨ Abriendo primer chat disponible: ${firstThreadText.slice(0, 50)}...`);
      }
      
      // Hacer clic en el chat
      await selectedThread.click();
      await SLEEP(2000);
      
      // Verificar que se abri√≥ correctamente
      const messageInput = this.page.locator(S.chat.messageInput);
      if (await messageInput.count() > 0) {
        console.log('‚úÖ Chat abierto correctamente');
        this.currentChat = selectedThread;
        return true;
      } else {
        console.log('‚ùå No se pudo abrir el chat');
        return false;
      }
      
    } catch (error) {
      console.error('‚ùå Error abriendo chat:', error.message);
      return false;
    }
  }

  async readLastMessage() {
    console.log('üìñ Leyendo √∫ltimo mensaje...');
    
    try {
      const lastMessage = this.page.locator(S.chat.lastInboundMsg);
      let lastMessageText = '';
      
      if (await lastMessage.count() > 0) {
        lastMessageText = await lastMessage.last().innerText().catch(() => '');
        console.log(`üìù √öltimo mensaje: "${lastMessageText.slice(0, 100)}..."`);
      } else {
        console.log('üìù No hay mensajes previos');
      }
      
      return lastMessageText;
      
    } catch (error) {
      console.error('‚ùå Error leyendo mensaje:', error.message);
      return '';
    }
  }

  async generateResponse(lastMessage, clientName = 'Cliente') {
    console.log('üß† Generando respuesta con IA...');
    
    try {
      const payload = {
        userId: 'Natsuki',
        threadId: clientName,
        lastMsg: lastMessage || 'Hola, ¬øc√≥mo est√°s?',
        mode: 'normal'
      };
      
      const { data } = await axios.post(process.env.BACKEND_URL + '/api/reply', payload);
      const reply = data?.reply || '¬°Hola! ¬øC√≥mo est√°s hoy? üòä Me encantar√≠a charlar contigo! üíï';
      
      console.log(`üí¨ Respuesta generada: "${reply}"`);
      return reply;
      
    } catch (error) {
      console.error('‚ùå Error generando respuesta:', error.message);
      return '¬°Hola! ¬øC√≥mo est√°s hoy? üòä Me encantar√≠a charlar contigo! üíï';
    }
  }

  async sendMessage(message) {
    console.log('üì§ Enviando mensaje...');
    
    try {
      // Buscar campo de entrada
      const input = this.page.locator(S.chat.messageInput).first();
      if (await input.count() === 0) {
        console.log('‚ùå No se encontr√≥ campo de entrada de mensaje');
        return false;
      }
      
      // Hacer clic en el campo
      await input.click();
      await SLEEP(500);
      
      // Escribir el mensaje con delays naturales
      console.log('‚úçÔ∏è Escribiendo mensaje...');
      for (const ch of message) {
        await input.type(ch, { delay: jitter(30, 80) });
      }
      
      await SLEEP(jitter(500, 1000));
      
      // Enviar el mensaje
      const sendButton = this.page.locator(S.chat.sendButton).first();
      if (await sendButton.count() > 0) {
        await sendButton.click();
      } else {
        await input.press('Enter');
      }
      
      console.log('‚úÖ Mensaje enviado exitosamente!');
      
      // Esperar a que se env√≠e
      await SLEEP(2000);
      
      return true;
      
    } catch (error) {
      console.error('‚ùå Error enviando mensaje:', error.message);
      return false;
    }
  }

  async waitForResponse(timeout = 10000) {
    console.log('‚è≥ Esperando respuesta...');
    
    try {
      const startTime = Date.now();
      let lastMessageCount = await this.page.locator(S.chat.lastInboundMsg).count();
      
      while (Date.now() - startTime < timeout) {
        await SLEEP(1000);
        
        const currentMessageCount = await this.page.locator(S.chat.lastInboundMsg).count();
        if (currentMessageCount > lastMessageCount) {
          const newMessage = await this.page.locator(S.chat.lastInboundMsg).last().innerText().catch(() => '');
          console.log(`üì® Nueva respuesta recibida: "${newMessage.slice(0, 100)}..."`);
          return newMessage;
        }
      }
      
      console.log('‚è∞ Timeout esperando respuesta');
      return null;
      
    } catch (error) {
      console.error('‚ùå Error esperando respuesta:', error.message);
      return null;
    }
  }

  async contactClient(targetUsername = null) {
    console.log(`üéØ Iniciando contacto${targetUsername ? ` con ${targetUsername}` : ' con cliente'}...\n`);
    
    try {
      // 1. Inicializar
      if (!await this.init()) {
        return false;
      }
      
      // 2. Login
      if (!await this.login()) {
        return false;
      }
      
      // 3. Navegar al chat
      if (!await this.navigateToChat()) {
        return false;
      }
      
      // 4. Buscar y abrir chat
      if (!await this.findAndOpenChat(targetUsername)) {
        return false;
      }
      
      // 5. Leer √∫ltimo mensaje
      const lastMessage = await this.readLastMessage();
      
      // 6. Generar respuesta
      const response = await this.generateResponse(lastMessage, targetUsername || 'Cliente');
      
      // 7. Enviar mensaje
      if (await this.sendMessage(response)) {
        console.log('üéâ Mensaje enviado exitosamente!');
        
        // 8. Esperar respuesta opcional
        await this.waitForResponse(5000);
        
        console.log('\n‚úÖ Proceso completado exitosamente!');
        return true;
      }
      
      return false;
      
    } catch (error) {
      console.error('‚ùå Error en el proceso:', error.message);
      return false;
    } finally {
      // No cerrar el navegador para que el usuario pueda ver el resultado
      console.log('üåê Navegador mantenido abierto para inspecci√≥n...');
    }
  }

  async close() {
    if (this.browser) {
      await this.browser.close();
      console.log('üîí Navegador cerrado');
    }
  }
}

// Funci√≥n principal
async function main() {
  const bot = new SmartBot();
  
  try {
    // Contactar a missy12 espec√≠ficamente
    await bot.contactClient('missy12');
  } catch (error) {
    console.error('‚ùå Error en el proceso principal:', error.message);
  }
}

// Ejecutar si es el archivo principal
if (require.main === module) {
  main();
}

module.exports = { SmartBot };
